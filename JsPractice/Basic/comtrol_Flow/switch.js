//------------------------------for number-------------------------------------
const key = 2;

switch (key) {
    case 1:
        console.log("value 1");
        break;
    case 2:
        console.log("value 2");
        break;
    case 3:
        console.log("value 3");
        break;
    case 4:
        console.log("value 4");
        break;

    default:
        console.log("the value doesn't match");
        
        break;
}

